    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(jtClientesCadastrados.getSelectedRow() != -1){
            
            int resposta = JOptionPane.showConfirmDialog(
               null, 
               "deseja realmente excluir esse contato?", 
               "Confirmação de exclusão", 
               JOptionPane.YES_OPTION);
            
            if(resposta == 0){
            
            DefaultTableModel dtmContatos = (DefaultTableModel) jtClientesCadastrados.getModel();
            dtmContatos.removeRow(jtClientesCadastrados.getSelectedRow());
            JOptionPane.showMessageDialog(null, "Cliente excluido com sucesso!");
                botoesEdicao(false);
                habilitarCaixas(false);
                apagarTudo();
            }else{
                JOptionPane.showMessageDialog(null, "Operação cancelada!");
            }
            
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

