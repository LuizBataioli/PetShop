    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        String nomeCliente = jtfNomeClienteDigitado.getText();
        String telefoneCliente = jtfTelefoneDigitado.getText();
        String emailCliente = jtfEmailClienteDigitado.getText();
        String tipoPet = jtfTipoPetDigitado.getText();
        String nomePet = jtfNomePetDigitado.getText();
        String racaPet = jtfRacaPetDigitado.getText();
        String idadePet = jtfIdadePetDigitada.getText();
        String tipoServico = cbxTipoServico.getSelectedItem().toString();
        String status = "Em andamento";
        
        
        int resposta = JOptionPane.showConfirmDialog(
               null, 
               "deseja realmente concluir o cadastro de " + nomeCliente + "?", 
               "Conrimação de cadastro", 
               JOptionPane.YES_OPTION);
        
       if(resposta == 0){
           
           DefaultTableModel dtmContatos = (DefaultTableModel) jtClientesCadastrados.getModel();
           Object[] dados = {nomeCliente, telefoneCliente, emailCliente, FormatacaoData(), tipoServico, tipoPet, nomePet, racaPet, idadePet, status};
           dtmContatos.addRow(dados);
           
           JOptionPane.showMessageDialog(null, "O cadastro foi concluído!");
           habilitarBotoes(false);
           habilitarCaixas(false);
           apagarTudo();
       }else{
           JOptionPane.showMessageDialog(null, "Operação cancelada");
           
       }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void jtClientesCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClientesCadastradosMouseClicked
        if(jtClientesCadastrados.getSelectedRow() != -1){
            
            int resposta = JOptionPane.showConfirmDialog(
               null, 
               "deseja realmente editar algo neste cliente? ", 
               "Confirmação de edição", 
               JOptionPane.YES_NO_OPTION);
            
            if(resposta == JOptionPane.YES_OPTION){
            
                jtfNomeClienteDigitado.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 0).toString());
                jtfTelefoneDigitado.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 1).toString());
                jtfEmailClienteDigitado.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 2).toString());
                cbxTipoServico.setSelectedItem(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 4).toString());
                jtfTipoPetDigitado.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 5).toString());
                jtfNomePetDigitado.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 6).toString());
                jtfRacaPetDigitado.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 7).toString());
                jtfIdadePetDigitada.setText(jtClientesCadastrados.getValueAt(jtClientesCadastrados.getSelectedRow(), 8).toString());
                habilitarBotoes(false);
                botoesEdicao(true);
                habilitarCaixas(true);
                
            }else{
                JOptionPane.showMessageDialog(null, "Operação cancelada!");
            }
            

        }
    }//GEN-LAST:event_jtClientesCadastradosMouseClicked

